        public static string PasswordHashed(this string password, string passwordSalt)
        {
            string concatSaltAndPassword = string.Concat(password, passwordSalt);
            return FormsAuthentication.HashPasswordForStoringInConfigFile(concatSaltAndPassword, "sha1");
        }

        // cắt một chuỗi theo số ký tự cần lấy và thêm ký tự mặc định vào cuối mỗi chuỗi khi đã cắt
        public static string CatChuoi(this string str, int maxLength, string endString)
        {
            if (str == null) return "";
            if (str.Length > maxLength)
            {
                str = str.Substring(0, maxLength) + endString;
            }
            return str;
        }

        //Kiểm tra xem ảnh có trên hệ thống hông nếu không thì hiển thị ảnh mặc định
        public static string CheckExistImage(this string imgPath)
        {
            var img = imgPath;
            if (!File.Exists(System.Web.HttpContext.Current.Server.MapPath(imgPath)))
            {
                img = "/Content/images/imgdefalt.jpg";
            }
            return img;
